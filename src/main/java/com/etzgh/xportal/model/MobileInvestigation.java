package com.etzgh.xportal.model;

import java.io.Serializable;
import javax.persistence.Entity;
import javax.persistence.Id;
import javax.xml.bind.annotation.XmlRootElement;

/**
 *
 * @author sunkwa-arthur
 */
@Entity
@XmlRootElement
public class MobileInvestigation implements Serializable {

    @Id
    private String id;
    private String mobile_no;
    private String destination_no;
    private String channel;
    private String message;
    private String ticket_id;
    private String unique_transid;
    private String checkvalue;
    private String msg_date;
    private String type;
    private String appid;

    public String getAppid() {
        return appid;
    }

    public void setAppid(String appid) {
        this.appid = appid;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getMobile_no() {
        return mobile_no;
    }

    public void setMobile_no(String mobile_no) {
        this.mobile_no = mobile_no;
    }

    public String getDestination_no() {
        return destination_no;
    }

    public void setDestination_no(String destination_no) {
        this.destination_no = destination_no;
    }

    public String getChannel() {
        return channel;
    }

    public void setChannel(String channel) {
        this.channel = channel;
    }

    public String getMessage() {
        return message;
    }

    public void setMessage(String message) {
        this.message = message;
    }

    public String getTicket_id() {
        return ticket_id;
    }

    public void setTicket_id(String ticket_id) {
        this.ticket_id = ticket_id;
    }

    public String getUnique_transid() {
        return unique_transid;
    }

    public void setUnique_transid(String unique_transid) {
        this.unique_transid = unique_transid;
    }

    public String getCheckvalue() {
        return checkvalue;
    }

    public void setCheckvalue(String checkvalue) {
        this.checkvalue = checkvalue;
    }

    public String getMsg_date() {
        return msg_date;
    }

    public void setMsg_date(String msg_date) {
        this.msg_date = msg_date;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    @Override
    public String toString() {
        return "MobileInvestigation{" + "id=" + id + ", mobile_no=" + mobile_no + ", destination_no=" + destination_no + ", channel=" + channel + ", message=" + message + ", ticket_id=" + ticket_id + ", unique_transid=" + unique_transid + ", checkvalue=" + checkvalue + ", msg_date=" + msg_date + ", type=" + type + ", appid=" + appid + '}';
    }

}
